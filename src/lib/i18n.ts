/**
 * Translate a key to the current locale string.
 * Falls back to Russian if the key is not found in the current locale.
 */
export function t(key: string, locale?: Locale): string {
  const loc = locale || "ru";
  return translations[loc]?.[key] || translations.ru[key] || key;
}

/**
 * Hook-like helper: returns the current locale and a setter from the store.
 * Use inside React components only.
 */
export function useLocale() {
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const { locale, setLocale } = require("./store").useAppStore.getState();
  return { locale, setLocale };
}
